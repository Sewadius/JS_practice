// Метод sort() сортирует элементы
// Возвращает отсортированный массив

let items = ["Меч", "Щит", "Свиток", "Кольцо"];
items.sort();

// [ 'Кольцо', 'Меч', 'Свиток', 'Щит' ]
console.log(items);

// Сортировка по убыванию
items.reverse();

// [ 'Щит', 'Свиток', 'Меч', 'Кольцо' ]
console.log(items);
