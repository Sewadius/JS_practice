// Числовой тип данных
'use strict';

const million = 1_000_000;      // Можно разделять разряды
console.log(million);           // 1000000

const mil = 1e6;
console.log(mil);               // 1000000

const micro = 1e-6;
console.log(micro);             // 0.000001, микро

const little = 1e-7;
console.log(little);            // слишком маленькое, 1e-7
