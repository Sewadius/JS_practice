// Другие системы счисления
'use strict';

console.log(0xAF);      // 175
console.log(0b101);     // 5
console.log(0o77);      // 63

// Дробные числа
console.log(0.5);       // 0.5

// Два нуля
console.log(0, -0);     // 0 -0

// Особенность нулей
console.log(1 / 0);     // Infinity
console.log(1 / -0);    // -Infinity
