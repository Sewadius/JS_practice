// Округление чисел
'use strict';

// Округление как в математике
console.log(Math.round(24.23));     // 24

// Округление вниз/вверх
console.log(Math.floor(24.89));     // 24
console.log(Math.ceil(24.21));      // 25

// Отсекает дробную часть
console.log(Math.trunc(25.99));     // 25

// Отрицательные числа
console.log(Math.floor(-24.33));    // -25
console.log(Math.trunc(-24.33));    // -24
